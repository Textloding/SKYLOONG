static bool connectToWiFi()
{
    if (WiFi.status() != WL_CONNECTED)
    {
        GUI::toast(_tr(I18N_ID_CONNECTING));
        if (WiFiMgr.autoConnectSaved(6000)) {
            if (hal.NTPSync()) {
                hal.time_sync = true;
            }
        } else {
            GUI::toast(_tr(I18N_ID_CONNECT_FAILED));
            return false;
        }
    }
    return true;
}
void AppWeather::loop()
{
    if (i18n::getLanguage() == 1) {
        xSemaphoreGive(appManagerLite._binary_switchApp);
        return;
    }
    if (!hal.weather_enable) {
        xSemaphoreGive(appManagerLite._binary_switchApp);
        return;
    }

    if ((hal.weather_update == false && millis() - last_millis > 5000) || (millis() - last_millis > 120000))
    {
        apikey = app_settings_save.weather_secret;
        city = app_settings_save.weather_city;
        hal.weather_update = true;
        last_millis = millis();
        if (connectToWiFi() == false)
            return;
        if (getWeather() == false)
        {
            GUI::toast("天气获取失败");
        }
        else
        {
            hal.LOCKLV();
            updateWeather();
            hal.UNLOCKLV();
            loadWeather = true;
            ++weather_update_cnt;
            if (weather_update_cnt % 5 == 0)
            {
                File f = LittleFS.open(".weather", "w");
                f.write((const uint8_t *)&weatherData, sizeof(weatherData));
                f.close();
            }
        }
    }
}

