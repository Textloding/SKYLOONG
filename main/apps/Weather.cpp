static String trimTrailingSlash(String value)
{
    value.trim();
    while (value.endsWith("/"))
        value.remove(value.length() - 1);
    return value;
}

static String weatherBaseUrl(const char *fallback)
{
    String base = weatherEndpoint;
    base.trim();
    if (base.length() == 0)
        base = fallback;
    if (!base.startsWith("http://") && !base.startsWith("https://"))
        base = String("https://") + base;
    return trimTrailingSlash(base);
}

static String urlEncode(const String &value)
{
    const char *hex = "0123456789ABCDEF";
    String out;
    for (size_t i = 0; i < value.length(); i++)
    {
        uint8_t c = (uint8_t)value[i];
        if ((c >= 'a' && c <= 'z') || (c >= 'A' && c <= 'Z') || (c >= '0' && c <= '9') || c == '-' || c == '_' || c == '.' || c == '~')
        {
            out += (char)c;
        }
        else if (c == ' ')
        {
            out += "%20";
        }
        else
        {
            out += '%';
            out += hex[(c >> 4) & 0x0f];
            out += hex[c & 0x0f];
        }
    }
    return out;
}

struct QueryParam
{
    String key;
    String value;
    bool hasValue;
};

static String canonicalPathAndParameters(const String &url)
{
    int schemeEnd = url.indexOf("://");
    int hostStart = schemeEnd >= 0 ? schemeEnd + 3 : 0;
    int pathStart = url.indexOf('/', hostStart);
    String pathAndQuery = pathStart >= 0 ? url.substring(pathStart) : "/";
    int queryStart = pathAndQuery.indexOf('?');
    if (queryStart < 0)
        return pathAndQuery.length() > 0 ? pathAndQuery : "/";

    String path = pathAndQuery.substring(0, queryStart);
    String query = pathAndQuery.substring(queryStart + 1);
    QueryParam params[16];
    size_t count = 0;
    int start = 0;
    while (start <= query.length() && count < sizeof(params) / sizeof(params[0]))
    {
        int end = query.indexOf('&', start);
        if (end < 0)
            end = query.length();
        String part = query.substring(start, end);
        if (part.length() > 0)
        {
            int eq = part.indexOf('=');
            params[count].hasValue = eq >= 0;
            params[count].key = eq >= 0 ? part.substring(0, eq) : part;
            params[count].value = eq >= 0 ? part.substring(eq + 1) : "";
            count++;
        }
        start = end + 1;
    }
    for (size_t i = 0; i < count; i++)
    {
        for (size_t j = i + 1; j < count; j++)
        {
            if (params[j].key < params[i].key)
            {
                QueryParam tmp = params[i];
                params[i] = params[j];
                params[j] = tmp;
            }
        }
    }
    String canonical = path.length() > 0 ? path : "/";
    for (size_t i = 0; i < count; i++)
    {
        canonical += (i == 0) ? "?" : "&";
        canonical += params[i].key;
        if (params[i].hasValue && params[i].value.length() > 0)
        {
            canonical += "=";
            canonical += params[i].value;
        }
    }
    return canonical;
}

static String hmacSha256Base64(const String &secret, const String &message)
{
    unsigned char digest[32];
    const mbedtls_md_info_t *info = mbedtls_md_info_from_type(MBEDTLS_MD_SHA256);
    if (info == NULL)
        return "";
    if (mbedtls_md_hmac(info,
                        (const unsigned char *)secret.c_str(),
                        secret.length(),
                        (const unsigned char *)message.c_str(),
                        message.length(),
                        digest) != 0)
    {
        return "";
    }

    unsigned char encoded[64];
    size_t olen = 0;
    if (mbedtls_base64_encode(encoded, sizeof(encoded), &olen, digest, sizeof(digest)) != 0)
        return "";
    encoded[olen] = '\0';
    return String((const char *)encoded);
}

static bool addAliyunDigestAuthHeaders(HTTPClient &http, const String &url, const char *appKey, const char *appSecret)
{
    if (appKey == NULL || appSecret == NULL || strlen(appKey) == 0 || strlen(appSecret) == 0)
        return false;
    static const char accept[] = "application/json";
    static const char signatureHeaders[] = "x-ca-key,x-ca-signature-method";

    String canonicalHeaders = String("x-ca-key:") + appKey + "\n" +
                              "x-ca-signature-method:HmacSHA256\n";
    String stringToSign = String("GET\n") +
                          accept + "\n" +
                          "\n" +
                          "\n" +
                          "\n" +
                          canonicalHeaders +
                          canonicalPathAndParameters(url);
    String signature = hmacSha256Base64(appSecret, stringToSign);
    if (signature.length() == 0)
    {
        snprintf(lastWeatherError, sizeof(lastWeatherError), "阿里云签名生成失败");
        return false;
    }

    http.addHeader("Accept", accept);
    http.addHeader("X-Ca-Key", appKey);
    http.addHeader("X-Ca-Signature-Method", "HmacSHA256");
    http.addHeader("X-Ca-Signature-Headers", signatureHeaders);
    http.addHeader("X-Ca-Signature", signature);
    return true;
}

static bool fetchJson(const String &url, cJSON **root, int *status = NULL, const char *appCode = NULL, const char *appKey = NULL, const char *appSecret = NULL)
{
    if (root == NULL)
        return false;
    *root = NULL;
    lastWeatherHttpStatus = 0;
    lastWeatherError[0] = '\0';

    HTTPClient http;
    WiFiClientSecure secure;
    bool okBegin = false;
    if (url.startsWith("https://"))
    {
        secure.setInsecure();
        okBegin = http.begin(secure, url);
    }
    else
    {
        okBegin = http.begin(url);
    }
    if (!okBegin)
    {
        ESP_LOGE("Weather", "http begin failed: %s", url.c_str());
        snprintf(lastWeatherError, sizeof(lastWeatherError), "HTTP 初始化失败");
        return false;
    }
    if (appCode != NULL && strlen(appCode) > 0)
    {
        String auth = String("APPCODE ") + appCode;
        http.addHeader("Authorization", auth);
    }
    else if (appKey != NULL && strlen(appKey) > 0 && appSecret != NULL && strlen(appSecret) > 0)
    {
        if (!addAliyunDigestAuthHeaders(http, url, appKey, appSecret))
        {
            http.end();
            return false;
        }
    }
    http.setTimeout(9000);
    int httpCode = http.GET();
    if (status != NULL)
        *status = httpCode;
    lastWeatherHttpStatus = httpCode;
    String payload = http.getString();
    http.end();
    if (httpCode != 200)
    {
        ESP_LOGW("Weather", "request failed: http=%d url=%s body=%s", httpCode, url.c_str(), payload.substring(0, 160).c_str());
        snprintf(lastWeatherError, sizeof(lastWeatherError), "HTTP %d: %s", httpCode, payload.substring(0, 80).c_str());
        return false;
    }
    *root = cJSON_Parse(payload.c_str());
    if (*root == NULL)
    {
        ESP_LOGE("Weather", "json parse failed: %s", payload.substring(0, 160).c_str());
        snprintf(lastWeatherError, sizeof(lastWeatherError), "JSON 解析失败: %s", payload.substring(0, 80).c_str());
        return false;
    }
    snprintf(lastWeatherError, sizeof(lastWeatherError), "OK");
    return true;
}

static cJSON *jsonObj(cJSON *parent, const char *key)
{
    cJSON *item = parent == NULL ? NULL : cJSON_GetObjectItem(parent, key);
    return (item != NULL && cJSON_IsObject(item)) ? item : NULL;
}

static cJSON *jsonArray(cJSON *parent, const char *key)
{
    cJSON *item = parent == NULL ? NULL : cJSON_GetObjectItem(parent, key);
    return (item != NULL && cJSON_IsArray(item)) ? item : NULL;
}

static const char *jsonString(cJSON *parent, const char *key)
{
    cJSON *item = parent == NULL ? NULL : cJSON_GetObjectItem(parent, key);
    return (item != NULL && cJSON_IsString(item) && item->valuestring != NULL) ? item->valuestring : NULL;
}

static int jsonInt(cJSON *parent, const char *key, int fallback = 0)
{
    cJSON *item = parent == NULL ? NULL : cJSON_GetObjectItem(parent, key);
    if (item == NULL)
        return fallback;
    if (cJSON_IsNumber(item))
        return item->valueint;
    if (cJSON_IsString(item) && item->valuestring != NULL)
        return atoi(item->valuestring);
    return fallback;
}

