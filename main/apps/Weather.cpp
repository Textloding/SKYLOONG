static bool connectToWiFi()
{
    if (WiFi.status() != WL_CONNECTED)
    {
        GUI::toast(_tr(I18N_ID_CONNECTING));
        WiFi.begin();
        if (WiFi.waitForConnectResult(6000) != WL_CONNECTED)
        {
            //hal.pref.putBool("wifi_succ", false);
            GUI::toast(_tr(I18N_ID_CONNECT_FAILED));
            return false;
        }
        //hal.pref.putBool("wifi_succ", true);
    }
    return true;
}
void AppWeather::loop()
{
    if ((first_update == false && millis() - last_millis > 5000) || (millis() - last_millis > 120000))
    {
        first_update = true;
        last_millis = millis();
        if (connectToWiFi() == false)
            return;
        if (getWeather() == false)
        {
            GUI::toast("天气获取失败");
        }
        else
        {
            hal.LOCKLV();
            updateWeather();
            hal.UNLOCKLV();
            ++weather_update_cnt;
            if (weather_update_cnt % 5 == 0)
            {
                File f = LittleFS.open(".weather", "w");
                f.write((const uint8_t *)&weatherData, sizeof(weatherData));
                f.close();
            }
        }
    }
}

void AppWeather::destroy()
{
    if (hal.server_started == false)
        WiFi.disconnect(true);
}
